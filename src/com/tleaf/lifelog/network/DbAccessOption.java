package com.tleaf.lifelog.network;

public class DbAccessOption {
	private String type;
	private String dbName;


	public String getDbName() {
		return dbName;
	}

	public void setDbName(String dbName) {
		this.dbName = dbName;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

}
